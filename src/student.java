    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(Id.getText());
            String sql = "SELECT * FROM STUDENT WHERE id = '"+stdId+"'";
            rs = stmt.executeQuery(sql);
            if(rs.next()){
                stdname.setText(rs.getString("stdName"));
                address.setText(rs.getString("Address"));
                contact.setText(rs.getString("Contact"));
                program.setText(rs.getString("Program"));
                year.setText(rs.getString("Year"));
                dob.setText(rs.getString("DateOfBirth"));



            }else{
                JOptionPane.showMessageDialog(null,"Record Not Found");
            }

        }catch (Exception e){JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButton4ActionPerformed

