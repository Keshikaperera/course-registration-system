    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            stmt = conn.createStatement();
            int id = Integer.parseInt(Id.getText());
            String stdName = stdname.getText();
            String Address = address.getText();
            String Contact = contact.getText();
            String Program = program.getText();
            String Year = year.getText();
            String DateOfBirth = dob.getText();

            String sql = "UPDATE STUDENT SET stdName = '"+stdName+"' ,Address = '"+Address+"',Contact = '"+Contact+"',Program = '"+Program+"',Year = '"+Year+"',DateOfBirth = '"+DateOfBirth+"'WHERE id = '"+id+"'";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Data successfully inserted!");

        }catch (Exception e) {JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButton1ActionPerformed

